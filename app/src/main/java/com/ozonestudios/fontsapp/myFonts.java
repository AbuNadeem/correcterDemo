package com.ozonestudios.fontsapp;

public class myFonts  {

    private String Name;
    private String Age;
    private String id;

    public myFonts(String id, String name, String age) {
        Name = name;
        Age = age;
        this.id = id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getAge() {
        return Age;
    }

    public void setAge(String age) {
        Age = age;
    }

    public String getID() {
        return id;
    }

    public void setID(String ID) {
        this.id = id;
    }
}